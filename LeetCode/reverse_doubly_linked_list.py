def reverse(llist):
    # Base case: if the list is empty or we reach the end, return the new head
    if llist is None:
        return None
    
    # Swap the next and prev pointers of the current node
    llist.prev, llist.next = llist.next, llist.prev
    
    # If head.prev is None, this means we are at the new head (formerly the tail), return it
    if llist.prev is None:
        return llist
    
    # Recurse to the next node (which is now head.prev after swapping)
    return reverse(llist.prev)

